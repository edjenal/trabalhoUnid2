package br.fanese.edu.cli.to;

public class ComposicaoTO {
	
	private int codProdutoQuimico;
	private int codRemedio;
	private String obs;
	
	public int getCodProdutoQuimico() {
		return codProdutoQuimico;
	}
	public void setCodProdutoQuimico(int codProdutoQuimico) {
		this.codProdutoQuimico = codProdutoQuimico;
	}
	public int getCodRemedio() {
		return codRemedio;
	}
	public void setCodRemedio(int codRemedio) {
		this.codRemedio = codRemedio;
	}
	public String getObs() {
		return obs;
	}
	public void setObs(String obs) {
		this.obs = obs;
	}

}
