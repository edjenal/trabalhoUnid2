package br.fanese.edu.cli.to;

public class RemedioTO {
	
	private int codRemedio;
	private String nomeComercial;
	private String principalIndicacao;
	private int codLaboratorio;
	
	public int getCodRemedio() {
		return codRemedio;
	}
	public void setCodRemedio(int codRemedio) {
		this.codRemedio = codRemedio;
	}
	public String getNomeComercial() {
		return nomeComercial;
	}
	public void setNomeComercial(String nomeComercial) {
		this.nomeComercial = nomeComercial;
	}
	public String getPrincipalIndicacao() {
		return principalIndicacao;
	}
	public void setPrincipalIndicacao(String principalIndicacao) {
		this.principalIndicacao = principalIndicacao;
	}
	public int getCodLaboratorio() {
		return codLaboratorio;
	}
	public void setCodLaboratorio(int codLaboratorio) {
		this.codLaboratorio = codLaboratorio;
	}
	
	

}
